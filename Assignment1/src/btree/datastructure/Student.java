package datastructure;

/**
 * This is a Student class, consists of three members name, redId and gpa
 */
public class Student {
	public String name;
	public int redId;
	public double gpa;

	public Student(String name, int redId, double gpa) {
		this.name = name;
		this.redId = redId;
		this.gpa = gpa;
	}
}
